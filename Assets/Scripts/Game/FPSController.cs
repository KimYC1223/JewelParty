using UnityEngine;

namespace MatchMatch.Game
{
    //===============================================================================================================================================
    //  FPS를 설정하는 클래스
    //===============================================================================================================================================
    public class FPSController : MonoBehaviour
    {
        #region PRIVATE_METHOD
        //===========================================================================================================================================
        //
        //  앱의 FPS를 60으로 설정
        //
        //===========================================================================================================================================
        private void Awake()
        {
            Application.targetFrameRate = 60;
        }
        #endregion
    }
}