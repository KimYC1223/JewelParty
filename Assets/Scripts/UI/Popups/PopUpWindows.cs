using UnityEngine;

namespace MatchMatch.UI.POPUP
{
    //===============================================================================================================================================
    //  팝업 윈도우들이 공통적으로 상속받는 추상 클래스
    //===============================================================================================================================================
    public abstract class PopUpWindows : MonoBehaviour
    {
        #region PUBLIC_METHOD
        //===========================================================================================================================================
        //
        //  팝업 윈도우가 열릴 때 마다 호출되는 메서드
        //
        //===========================================================================================================================================
        /// <summary>
        /// 팝업 윈도우가 열릴 때 마다 호출되는 메서드.
        /// </summary>
        public virtual void OnOpenWindow()
        {
            PopUpManager.Instance.CloseOtherWidnwos(this.gameObject.name);
        }

        //===========================================================================================================================================
        //
        //  팝업 윈도우를 닫는 메서드
        //
        //===========================================================================================================================================
        /// <summary>
        /// 팝업 윈도우를 닫는 메서드.
        /// </summary>
        public virtual void CloseWinodw()
        {
            this.gameObject.SetActive(false);
        }
        #endregion

        #region PRIVATE_METHOD
        //===========================================================================================================================================
        //
        //  OnEnable 라이프 사이클에 호출되는 메서드.
        //
        //===========================================================================================================================================
        private void OnEnable()
        {
            OnOpenWindow();                 // OnOpenWindow 메서드를 호출함
        }
        #endregion
    }
}
