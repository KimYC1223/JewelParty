using UnityEngine;

namespace MatchMatch.UI.POPUP
{
    //===============================================================================================================================================
    //  메뉴 UI를 제어하는 핸들러
    //===============================================================================================================================================
    public class MenuUIHandler : PopUpWindows
    {
        #region PUBLIC_METHOD
        //===========================================================================================================================================
        //
        //  게임 종료 메서드
        //
        //===========================================================================================================================================
        /// <summary>
        /// 게임을 종료하는 메서드.
        /// </summary>
        public void ExitGame()
        {
#if UNITY_EDITOR
            UnityEditor.EditorApplication.isPlaying = false;
#else
            Application.Quit();
#endif
        }
        #endregion
    }
}